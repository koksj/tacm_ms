package za.co.tacm;

public class Person {
    
    private String firstName;
    private String lastName;
    private String identityNumber;
    private String mobileNumber;
    private String physicalAddress;
    private String email;
    private String businessName;
    private String companyRegistrationNumber;
    private String province;
    
    public Person() {
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getIdentityNumber() {
        return identityNumber;
    }

    public void setIdentityNumber(String identityNumber) {
        this.identityNumber = identityNumber;
    }

    public String getMobileNumber() {
        return mobileNumber;
    }

    public void setMobileNumber(String mobileNumber) {
        this.mobileNumber = mobileNumber;
    }

    public String getPhysicalAddress() {
        return physicalAddress;
    }

    public void setPhysicalAddress(String physicalAddress) {
        this.physicalAddress = physicalAddress;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getBusinessName() {
        return businessName;
    }

    public void setBusinessName(String businessName) {
        this.businessName = businessName;
    }

    public String getCompanyRegistrationNumber() {
        return companyRegistrationNumber;
    }

    public void setCompanyRegistrationNumber(String companyRegistrationNumber) {
        this.companyRegistrationNumber = companyRegistrationNumber;
    }

    public String getProvince() {
        return province;
    }

    public void setProvince(String province) {
        this.province = province;
    }

    @Override
    public String toString() {
        return "Farmer [businessName=" + businessName + ", companyRegistrationNumber=" + companyRegistrationNumber
                + ", email=" + email + ", firstName=" + firstName + ", identityNumber=" + identityNumber + ", lastName="
                + lastName + ", mobileNumber=" + mobileNumber + ", physicalAddress=" + physicalAddress + ", province="
                + province + "]";
    }
    
}
